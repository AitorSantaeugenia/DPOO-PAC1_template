
public class Vehicle {
	
	private String nameVehicle;
	int priceVehicle;
	int numberSeats;
	private String typeVehicle;
	
	public String getNameVehicles() {
		return nameVehicle;
		
	}
	
	public void setNameVehicles(String nameVehicle) {
		this.nameVehicle = nameVehicle;
	}
	
	public int getPriceVehicles() {
		return priceVehicle;
		
	}
	
	public void setPriceVehicles(int priceVehicle) {
		this.priceVehicle = priceVehicle;
	}
	
	public int getNumberSeats() {
		return numberSeats;
	}
	
	public void setNumberSeats(int numberSeats) {
		this.numberSeats = numberSeats;
	}
	
	public String getTypeVehicles() {
		return typeVehicle;
	}
	
	public void setTypeVehicles(String typeVehicle) {
		this.typeVehicle = typeVehicle;
	}
}
